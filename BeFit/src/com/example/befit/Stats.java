package com.example.befit;

public class Stats {

	String date;
	int step;
	
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getStep() {
		return step;
	}
	public void setStep(int step) {
		this.step = step;
	}
	@Override
	public String toString() {
		return "Stats [date=" + date + ", step=" + step + "]";
	}
	
	
	
}
